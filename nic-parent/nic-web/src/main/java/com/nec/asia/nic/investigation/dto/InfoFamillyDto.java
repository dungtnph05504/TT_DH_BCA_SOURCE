package com.nec.asia.nic.investigation.dto;

public class InfoFamillyDto {
	private String fullName_F;
	private String dob_F;
	private String stage_F;
	private String gender_F;
	private String toFamilly;
	
	public InfoFamillyDto(String fullName_F, String dob_F, String stage_F,
			String gender_F, String toFamilly) {
		super();
		this.fullName_F = fullName_F;
		this.dob_F = dob_F;
		this.stage_F = stage_F;
		this.gender_F = gender_F;
		this.toFamilly = toFamilly;
	}
	
	public InfoFamillyDto() {
		super();
	}

	public String getFullName_F() {
		return fullName_F;
	}

	public void setFullName_F(String fullName_F) {
		this.fullName_F = fullName_F;
	}

	public String getDob_F() {
		return dob_F;
	}

	public void setDob_F(String dob_F) {
		this.dob_F = dob_F;
	}

	public String getStage_F() {
		return stage_F;
	}

	public void setStage_F(String stage_F) {
		this.stage_F = stage_F;
	}

	public String getGender_F() {
		return gender_F;
	}

	public void setGender_F(String gender_F) {
		this.gender_F = gender_F;
	}

	public String getToFamilly() {
		return toFamilly;
	}

	public void setToFamilly(String toFamilly) {
		this.toFamilly = toFamilly;
	}
	
	
	
}
