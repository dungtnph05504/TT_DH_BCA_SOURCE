package com.nec.asia.nic.util;

import java.util.Date;

public class PrintLocation {
	public long ID;
	public Date CreatedDate;
	public Date LastModifiedDate;

	public long CreatedBy;
	public long LastModifiedBy;
	public String Name;
	public String Code;
	public String Address;
	public String Description;
	public Integer Status;

	public PrintLocation() {
	}

	public PrintLocation(long ID, Date CreatedDate, Date LastModifiedDate,
			long CreatedBy, long LastModifiedBy, String Name, String Code,
			String Address, String Description, Integer Status) {
		this.ID = ID;
		this.CreatedDate = CreatedDate;
		this.LastModifiedDate = LastModifiedDate;
		this.CreatedBy = CreatedBy;
		this.LastModifiedBy = LastModifiedBy;
		this.Name = Name;
		this.Code = Code;
		this.Address = Address;
		this.Description = Description;
		this.Status = Status;
	}

	public long getID() {
		return this.ID;
	}

	public void setID(long ID) {
		this.ID = ID;
	}

	public Date getCreatedDate() {
		return this.CreatedDate;
	}

	public void setCreatedDate(Date CreatedDate) {
		this.CreatedDate = CreatedDate;
	}

	public Date getLastModifiedDate() {
		return this.LastModifiedDate;
	}

	public void setLastModifiedDate(Date LastModifiedDate) {
		this.LastModifiedDate = LastModifiedDate;
	}

	public long getCreatedBy() {
		return this.CreatedBy;
	}

	public void setCreatedBy(long CreatedBy) {
		this.CreatedBy = CreatedBy;
	}

	public long getLastModifiedBy() {
		return this.LastModifiedBy;
	}

	public void setLastModifiedBy(long LastModifiedBy) {
		this.LastModifiedBy = LastModifiedBy;
	}

	public String getName() {
		return this.Name;
	}

	public void setName(String Name) {
		this.Name = Name;
	}

	public String getCode() {
		return this.Code;
	}

	public void setCode(String Code) {
		this.Code = Code;
	}

	public String getAddress() {
		return this.Address;
	}

	public void setAddress(String Address) {
		this.Address = Address;
	}
	
	public String getDescription() {
		return this.Description;
	}

	public void setDescription(String Description) {
		this.Description = Description;
	}
	
	public Integer getStatus() {
		return this.Status;
	}

	public void setStatus(Integer Status) {
		this.Status = Status;
	}
	
	
}