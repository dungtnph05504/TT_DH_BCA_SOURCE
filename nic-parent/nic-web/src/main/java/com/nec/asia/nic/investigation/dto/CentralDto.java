package com.nec.asia.nic.investigation.dto;

import java.util.List;

public class CentralDto {
	
	private String siteCode;
	private List<String> dsTTCT;
	private int[] data;
	
	public CentralDto() {
		super();
	}
	public CentralDto(String siteCode, int[] data) {
		super();
		this.siteCode = siteCode;
		this.data = data;
	}
	
	public List<String> getDsTTCT() {
		return dsTTCT;
	}
	public void setDsTTCT(List<String> dsTTCT) {
		this.dsTTCT = dsTTCT;
	}
	public String getSiteCode() {
		return siteCode;
	}
	public void setSiteCode(String siteCode) {
		this.siteCode = siteCode;
	}
	public int[] getData() {
		return data;
	}
	public void setData(int[] data) {
		this.data = data;
	}
	
	
}
