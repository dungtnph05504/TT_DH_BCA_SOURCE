package com.nec.asia.nic.investigation.dto;

import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlRootElement(name="TransactionDocument")
@XmlType(name="TransactionDocument", propOrder={"docType", "serialNo", "docData", "minutiaData", "minutiaFormat"})
public class TransactionDocument {
	
	private String docType;
	private String serialNo;
	private String docData;
	private String minutiaData;
	private String minutiaFormat;
	
	
	public String getDocType() {
		return docType;
	}
	public void setDocType(String docType) {
		this.docType = docType;
	}
	public String getSerialNo() {
		return serialNo;
	}
	public void setSerialNo(String serialNo) {
		this.serialNo = serialNo;
	}
	public String getDocData() {
		return docData;
	}
	public void setDocData(String docData) {
		this.docData = docData;
	}
	public String getMinutiaData() {
		return minutiaData;
	}
	public void setMinutiaData(String minutiaData) {
		this.minutiaData = minutiaData;
	}
	public String getMinutiaFormat() {
		return minutiaFormat;
	}
	public void setMinutiaFormat(String minutiaFormat) {
		this.minutiaFormat = minutiaFormat;
	}
	
	
}
