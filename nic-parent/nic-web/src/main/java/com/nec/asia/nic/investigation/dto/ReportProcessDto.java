package com.nec.asia.nic.investigation.dto;

public class ReportProcessDto {
	
	private ReportProcessTranDto dto = new ReportProcessTranDto();
	private ImmiHistoryCountDto immi = new ImmiHistoryCountDto();
	private int amountIssue;
	private int amountIssueNot;
	private String siteGroup;
	
	public ReportProcessDto() {
		super();
	}

	public ReportProcessDto(ReportProcessTranDto dto, String siteGroup) {
		super();
		this.dto = dto;
		this.siteGroup = siteGroup;
	}

	public ReportProcessTranDto getDto() {
		return dto;
	}

	public void setDto(ReportProcessTranDto dto) {
		this.dto = dto;
	}

	public String getSiteGroup() {
		return siteGroup;
	}

	public void setSiteGroup(String siteGroup) {
		this.siteGroup = siteGroup;
	}

	public ImmiHistoryCountDto getImmi() {
		return immi;
	}

	public void setImmi(ImmiHistoryCountDto immi) {
		this.immi = immi;
	}

	public int getAmountIssue() {
		return amountIssue;
	}

	public void setAmountIssue(int amountIssue) {
		this.amountIssue = amountIssue;
	}

	public int getAmountIssueNot() {
		return amountIssueNot;
	}

	public void setAmountIssueNot(int amountIssueNot) {
		this.amountIssueNot = amountIssueNot;
	}

}
