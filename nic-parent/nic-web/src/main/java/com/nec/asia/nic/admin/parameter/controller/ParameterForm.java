package com.nec.asia.nic.admin.parameter.controller;

import java.util.Map;

/* 
* Modification History:
* 
* 26 Jan 2016 (Tue) : Add paraLobValue 
* 
*/
public class ParameterForm {

	private String paraScope;
	private String paraName;

	private String paraValue;
	private String paraDesc;
	
	private String paraType;
	private Boolean userAccessibleFlag;
	private Boolean adminAccessibleFlag;
	private String systemId;
	
	private Map<String, String> sysCodeMap;
	private String paraLobValue;
	
	/**
	 * @return the paraScope
	 */
	public String getParaScope() {
		return paraScope;
	}
	/**
	 * @param paraScope the paraScope to set
	 */
	public void setParaScope(String paraScope) {
		this.paraScope = paraScope;
	}
	/**
	 * @return the paraName
	 */
	public String getParaName() {
		return paraName;
	}
	/**
	 * @param paraName the paraName to set
	 */
	public void setParaName(String paraName) {
		this.paraName = paraName;
	}
	/**
	 * @return the paraValue
	 */
	public String getParaValue() {
		return paraValue;
	}
	/**
	 * @param paraValue the paraValue to set
	 */
	public void setParaValue(String paraValue) {
		this.paraValue = paraValue;
	}
	/**
	 * @return the paraDesc
	 */
	public String getParaDesc() {
		return paraDesc;
	}
	/**
	 * @param paraDesc the paraDesc to set
	 */
	public void setParaDesc(String paraDesc) {
		this.paraDesc = paraDesc;
	}
	/**
	 * @return the paraType
	 */
	public String getParaType() {
		return paraType;
	}
	/**
	 * @param paraType the paraType to set
	 */
	public void setParaType(String paraType) {
		this.paraType = paraType;
	}
	/**
	 * @return the userAccessibleFlag
	 */
	public Boolean getUserAccessibleFlag() {
		return userAccessibleFlag;
	}
	/**
	 * @param userAccessibleFlag the userAccessibleFlag to set
	 */
	public void setUserAccessibleFlag(Boolean userAccessibleFlag) {
		this.userAccessibleFlag = userAccessibleFlag;
	}
	/**
	 * @return the adminAccessibleFlag
	 */
	public Boolean getAdminAccessibleFlag() {
		return adminAccessibleFlag;
	}
	/**
	 * @param adminAccessibleFlag the adminAccessibleFlag to set
	 */
	public void setAdminAccessibleFlag(Boolean adminAccessibleFlag) {
		this.adminAccessibleFlag = adminAccessibleFlag;
	}
	/**
	 * @return the systemId
	 */
	public String getSystemId() {
		return systemId;
	}
	/**
	 * @param systemId the systemId to set
	 */
	public void setSystemId(String systemId) {
		this.systemId = systemId;
	}
	/**
	 * @return the sysCodeMap
	 */
	public Map<String, String> getSysCodeMap() {
		return sysCodeMap;
	}
	/**
	 * @param sysCodeMap the sysCodeMap to set
	 */
	public void setSysCodeMap(Map<String, String> sysCodeMap) {
		this.sysCodeMap = sysCodeMap;
	}
	/**
	 * @return the paraLobValue
	 */
	public String getParaLobValue() {
		return paraLobValue;
	}
	/**
	 * @param paraLobValue the paraLobValue to set
	 */
	public void setParaLobValue(String paraLobValue) {
		this.paraLobValue = paraLobValue;
	}
	@Override
	public String toString() {
		return "ParameterForm [paraScope=" + paraScope + ", paraName="
				+ paraName + ", paraValue=" + paraValue + ", paraDesc="
				+ paraDesc + ", paraType=" + paraType + ", userAccessibleFlag="
				+ userAccessibleFlag + ", adminAccessibleFlag="
				+ adminAccessibleFlag + ", systemId=" + systemId
				+ ", sysCodeMap=" + sysCodeMap + ", paraLobValue="
				+ paraLobValue + "]";
	}
}
