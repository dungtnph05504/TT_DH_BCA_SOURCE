package com.nec.asia.nic.framework.common.query;


/**
 * Criteria for specifying not equals condition.<BR>
 * From BAF framework
 * 
 * @author WL
 */
public class NotEqualCriteria<T> extends HbmCriteria<T> implements ValueCriteria<T> {
	
	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = 1L;
	
	/** The value. */
	private T value; 

	/**
	 * Instantiates a new not equal criteria.
	 */
	public NotEqualCriteria() {
		
	}
	
	/**
	 * Instantiates a new not equal criteria.
	 *
	 * @param value the value
	 */
	public NotEqualCriteria(T value) {
		this.value=value;
	}
	
	/**
	 * Gets the value.
	 *
	 * @return the value
	 */
	public T getValue() {
		return value;
	}

	/**
	 * Sets the value.
	 *
	 * @param value the value to set
	 */
	public void setValue(T value) {
		this.value = value;
	}	
}
