package com.nec.asia.nic.framework.common.query;


/**
 * Criteria for specifying in condition.<BR>
 * From BAF framework
 * 
 * @author WL
 */
public class InCriteria<T> extends HbmCriteria<T> {
	
	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = 1L;
	
	/** The value. */
	private T[] value;

	/**
	 * Instantiates a new equal criteria.
	 */
	public InCriteria() {
		
	}
	
	/**
	 * Instantiates a new equal criteria.
	 *
	 * @param value the value
	 */
	public InCriteria(T[] value) {
		this.value=value;
	}
	
	/**
	 * Gets the value.
	 *
	 * @return the value
	 */
	public T[] getValue() {
		return value;
	}

	/**
	 * Sets the value.
	 *
	 * @param value the value to set
	 */
	public void setValue(T[] value) {
		this.value = value;
	}


}
