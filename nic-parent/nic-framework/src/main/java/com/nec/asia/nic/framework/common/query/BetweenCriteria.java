package com.nec.asia.nic.framework.common.query;


/**
 * Criteria for specifying between condition.
 * From BAF framework
 * 
 * @author WL
 */
public class BetweenCriteria<T> extends HbmCriteria<T> {
	
	/** The Constant serialVersionUID. */
	private static final long serialVersionUID = 1L;
	
	/** The from value. */
	private T fromValue; 
	
	/** The to value. */
	private T toValue;

	
	/**
	 * Instantiates a new between criteria.
	 */
	public BetweenCriteria() {
		
	}
	
	
	/**
	 * Gets the from value.
	 *
	 * @return the fromValue
	 */
	public T getFromValue() {
		return fromValue;
	}
	
	/**
	 * Sets the from value.
	 *
	 * @param fromValue the fromValue to set
	 */
	public void setFromValue(T fromValue) {
		this.fromValue = fromValue;
	}
	
	/**
	 * Gets the to value.
	 *
	 * @return the toValue
	 */
	public T getToValue() {
		return toValue;
	}
	
	/**
	 * Sets the to value.
	 *
	 * @param toValue the toValue to set
	 */
	public void setToValue(T toValue) {
		this.toValue = toValue;
	}
	
}
