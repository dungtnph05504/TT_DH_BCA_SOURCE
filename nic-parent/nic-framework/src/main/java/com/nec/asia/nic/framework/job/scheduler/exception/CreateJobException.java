package com.nec.asia.nic.framework.job.scheduler.exception;

import com.nec.asia.nic.framework.exception.CoreException;


/**
 * The Class CreateJobException.
 *
 * @author boon_wui
 * @since 14 April 208
 * @version 1.0
 */
public class CreateJobException extends CoreException{

	/**
	 * The Constructor.
	 */
	public CreateJobException() {
		super("Failed to create Job.");
	}

	/**
	 * The Constructor.
	 * 
	 * @param message
	 *            the message
	 */
	public CreateJobException(String message) {
		super(message);
	}

	/**
	 * The Constructor.
	 * 
	 * @param message
	 *            the message
	 * @param cause
	 *            the cause
	 */
	public CreateJobException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * The Constructor.
	 * 
	 * @param cause
	 *            the cause
	 */
	public CreateJobException(Throwable cause) {
		super("Failed to create job. Cause: "+cause.getMessage(),cause);
	}
}
