package com.nec.asia.nic.framework.storeforward;


/**
 * The Checked Exception of Store and Forward.
 * 
 * @author bright_zheng
 * @since 27 May 2013
 *
 */
public class StoreForwardException extends Exception {
	private static final long serialVersionUID = 5813379729167774787L;
	public StoreForwardException(String message) {
		super(message);
	}
	public StoreForwardException(Throwable th) {
		super(th);
	}
	public StoreForwardException(String message, Throwable th) {
		super(message,th);
	}
}
