package com.nec.asia.nic.comp.trans.dto;

public class AttachStorageDto {
	private String tenFile;
	private String loaiFile;
	private int stt;
	private String moTaFile;
	private String transactionId;
	public AttachStorageDto(String tenFile, String loaiFile, int stt,
			String moTaFile) {
		super();
		this.tenFile = tenFile;
		this.loaiFile = loaiFile;
		this.stt = stt;
		this.moTaFile = moTaFile;
	}
	
	public String getTransactionId() {
		return transactionId;
	}

	public void setTransactionId(String transactionId) {
		this.transactionId = transactionId;
	}

	public AttachStorageDto() {
		super();
	}
	public String getTenFile() {
		return tenFile;
	}
	public void setTenFile(String tenFile) {
		this.tenFile = tenFile;
	}
	public String getLoaiFile() {
		return loaiFile;
	}
	public void setLoaiFile(String loaiFile) {
		this.loaiFile = loaiFile;
	}
	public int getStt() {
		return stt;
	}
	public void setStt(int stt) {
		this.stt = stt;
	}
	public String getMoTaFile() {
		return moTaFile;
	}
	public void setMoTaFile(String moTaFile) {
		this.moTaFile = moTaFile;
	}
	
}
