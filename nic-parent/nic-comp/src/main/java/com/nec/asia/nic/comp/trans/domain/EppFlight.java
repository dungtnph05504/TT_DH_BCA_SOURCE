package com.nec.asia.nic.comp.trans.domain;

import java.io.Serializable;
import java.util.Date;

public class EppFlight implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Long id;
	private String createBy;
	private String lastModifiedBy;
	private Date createDate;
	private Date lastModifiedDate;
	private String flightNo;
	private String flightRouterCode;
	private String airlineCode;
	private String type;
	private String name;
	private String note;
	private Date closedTime;
	
	public EppFlight(){
		
	}

	public EppFlight(Long id, String createBy, String lastModifiedBy,
			Date createDate, Date lastModifiedDate, String flightNo,
			String flightRouterCode, String airlineCode, String type,
			String name, String note, Date closedTime) {
		super();
		this.id = id;
		this.createBy = createBy;
		this.lastModifiedBy = lastModifiedBy;
		this.createDate = createDate;
		this.lastModifiedDate = lastModifiedDate;
		this.flightNo = flightNo;
		this.flightRouterCode = flightRouterCode;
		this.airlineCode = airlineCode;
		this.type = type;
		this.name = name;
		this.note = note;
		this.closedTime = closedTime;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public String getLastModifiedBy() {
		return lastModifiedBy;
	}

	public void setLastModifiedBy(String lastModifiedBy) {
		this.lastModifiedBy = lastModifiedBy;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public Date getLastModifiedDate() {
		return lastModifiedDate;
	}

	public void setLastModifiedDate(Date lastModifiedDate) {
		this.lastModifiedDate = lastModifiedDate;
	}

	public String getFlightNo() {
		return flightNo;
	}

	public void setFlightNo(String flightNo) {
		this.flightNo = flightNo;
	}

	public String getFlightRouterCode() {
		return flightRouterCode;
	}

	public void setFlightRouterCode(String flightRouterCode) {
		this.flightRouterCode = flightRouterCode;
	}

	public String getAirlineCode() {
		return airlineCode;
	}

	public void setAirlineCode(String airlineCode) {
		this.airlineCode = airlineCode;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public Date getClosedTime() {
		return closedTime;
	}

	public void setClosedTime(Date closedTime) {
		this.closedTime = closedTime;
	}
	
	
	
}
