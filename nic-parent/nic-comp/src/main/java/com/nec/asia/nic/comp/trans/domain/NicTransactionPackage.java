package com.nec.asia.nic.comp.trans.domain;

import java.io.Serializable;

public class NicTransactionPackage implements Serializable{
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Long id;
	private String packageId;
	private String transactionId;
	private String stage;
	private int typeList;
	private String noteApprove;
	private String noteLeaderApprove;
	private String offerStage;
	
	public NicTransactionPackage() {
		super();
	}
	
	public NicTransactionPackage(String packageId, String transactionId,
			String stage, int typeList) {
		super();
		this.packageId = packageId;
		this.transactionId = transactionId;
		this.stage = stage;
		this.typeList = typeList;
	}
	
	public NicTransactionPackage(Long id, String packageId,
			String transactionId, String stage, int typeList) {
		super();
		//this.id = id;
		this.packageId = packageId;
		this.transactionId = transactionId;
		this.stage = stage;
		this.typeList = typeList;
	}
	
	public String getOfferStage() {
		return offerStage;
	}

	public void setOfferStage(String offerStage) {
		this.offerStage = offerStage;
	}

	public String getNoteLeaderApprove() {
		return noteLeaderApprove;
	}

	public void setNoteLeaderApprove(String noteLeaderApprove) {
		this.noteLeaderApprove = noteLeaderApprove;
	}

	public String getNoteApprove() {
		return noteApprove;
	}

	public void setNoteApprove(String noteApprove) {
		this.noteApprove = noteApprove;
	}

	public int getTypeList() {
		return typeList;
	}

	public void setTypeList(int typeList) {
		this.typeList = typeList;
	}

	public String getPackageId() {
		return packageId;
	}
	
	public void setPackageId(String packageId) {
		this.packageId = packageId;
	}
	
	public String getTransactionId() {
		return transactionId;
	}
	
	public void setTransactionId(String transactionId) {
		this.transactionId = transactionId;
	}
	
	public String getStage() {
		return stage;
	}
	
	public void setStage(String stage) {
		this.stage = stage;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}
	
	
}
