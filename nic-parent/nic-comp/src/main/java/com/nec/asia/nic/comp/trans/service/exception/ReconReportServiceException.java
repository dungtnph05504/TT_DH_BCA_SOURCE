package com.nec.asia.nic.comp.trans.service.exception;

import com.nec.asia.nic.framework.exception.CoreException;


/* 
 * Modification History:
 *  
 * 27 Nov 2013 (chris): init class
 */
public class ReconReportServiceException extends CoreException {

	/**
	 * The Constructor.
	 */
	public ReconReportServiceException() {
		super("Recon Report Service Exception encountered.");
	}

	/**
	 * The Constructor.
	 * 
	 * @param message the message
	 */
	public ReconReportServiceException(String message) {
		super(message);
	}

	/**
	 * The Constructor.
	 * 
	 * @param message the message
	 * @param cause the cause
	 */
	public ReconReportServiceException(String message, Throwable cause) {
		super(message, cause);
	}

	/**
	 * The Constructor.
	 * 
	 * @param cause the cause
	 */
	public ReconReportServiceException(Throwable cause) {
		super("Recon Report Service Exception encountered. Cause: " + cause.getMessage(), cause);
	}


	/**
	 * Instantiates a new TransactionServiceException exception.
	 *
	 * @param message the message
	 * @param errorCode the error code 
	 */
	public ReconReportServiceException(String message, String errorCode) {
		super(message, errorCode);
	}
	
	/**
	 * The Constructor.
	 *
	 * @param message the message
	 * @param errorCode the error code 
	 * @param cause the cause
	 */
	public ReconReportServiceException(String message, String errorCode, Throwable cause) {
		super(message, errorCode, cause);
	}

}
