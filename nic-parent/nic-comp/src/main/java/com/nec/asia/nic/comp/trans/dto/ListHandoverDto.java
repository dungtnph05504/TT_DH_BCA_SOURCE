package com.nec.asia.nic.comp.trans.dto;

public class ListHandoverDto {
    private String packageId;
    private String createTs;
    private String createBy;
    private String approverDate;
    private String approverName;
    private String approverContent;
    private String prpContent;
    private String description;
    
	public String getPrpContent() {
		return prpContent;
	}

	public void setPrpContent(String prpContent) {
		this.prpContent = prpContent;
	}

	public String getPackageId() {
		return packageId;
	}
	
	public void setPackageId(String packageId) {
		this.packageId = packageId;
	}
	
	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public String getCreateTs() {
		return createTs;
	}
	public void setCreateTs(String createTs) {
		this.createTs = createTs;
	}
	public String getApproverDate() {
		return approverDate;
	}
	public void setApproverDate(String approverDate) {
		this.approverDate = approverDate;
	}
	public String getApproverName() {
		return approverName;
	}
	public void setApproverName(String approverName) {
		this.approverName = approverName;
	}
	public String getApproverContent() {
		return approverContent;
	}
	public void setApproverContent(String approverContent) {
		this.approverContent = approverContent;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
    
}
