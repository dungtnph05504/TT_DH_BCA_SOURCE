package com.nec.asia.nic.comp.trans.domain;

import java.io.Serializable;
import java.util.Date;

public class EppPurpose implements Serializable {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Long id;
	private String createBy;
	private String lastModifiedBy;
	private Date createDate;
	private Date lastModifiedDate;
	private String purposeCode;
	private String title;
	private int status;
	private String note;
	private Date closedTime;
	
	public EppPurpose(){
		
	}
	
	public EppPurpose(Long id, String createBy, String lastModifiedBy,
			Date createDate, Date lastModifiedDate, String purposeCode,
			String title, int status, String note, Date closedTime) {
		super();
		this.id = id;
		this.createBy = createBy;
		this.lastModifiedBy = lastModifiedBy;
		this.createDate = createDate;
		this.lastModifiedDate = lastModifiedDate;
		this.purposeCode = purposeCode;
		this.title = title;
		this.status = status;
		this.note = note;
		this.closedTime = closedTime;
	}
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public String getCreateBy() {
		return createBy;
	}
	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}
	public String getLastModifiedBy() {
		return lastModifiedBy;
	}
	public void setLastModifiedBy(String lastModifiedBy) {
		this.lastModifiedBy = lastModifiedBy;
	}
	public Date getCreateDate() {
		return createDate;
	}
	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}
	public Date getLastModifiedDate() {
		return lastModifiedDate;
	}
	public void setLastModifiedDate(Date lastModifiedDate) {
		this.lastModifiedDate = lastModifiedDate;
	}
	public String getPurposeCode() {
		return purposeCode;
	}
	public void setPurposeCode(String purposeCode) {
		this.purposeCode = purposeCode;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	
	public int getStatus() {
		return status;
	}

	public void setStatus(int status) {
		this.status = status;
	}

	public String getNote() {
		return note;
	}
	public void setNote(String note) {
		this.note = note;
	}
	public Date getClosedTime() {
		return closedTime;
	}
	public void setClosedTime(Date closedTime) {
		this.closedTime = closedTime;
	}
}
