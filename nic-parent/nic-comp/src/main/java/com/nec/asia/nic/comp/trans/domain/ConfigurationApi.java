package com.nec.asia.nic.comp.trans.domain;

import java.io.Serializable;
import java.util.Date;

public class ConfigurationApi implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Long id;
	private String uriApi;
	private String nameApi;
	private String description;
	private Boolean closeAll;
	private String closeMember;
	private Date createDate;
	private String createBy;
	private Integer type;
	public ConfigurationApi() {
		super();
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getUriApi() {
		return uriApi;
	}

	public void setUriApi(String uriApi) {
		this.uriApi = uriApi;
	}

	public String getNameApi() {
		return nameApi;
	}

	public void setNameApi(String nameApi) {
		this.nameApi = nameApi;
	}

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public Boolean getCloseAll() {
		return closeAll;
	}

	public void setCloseAll(Boolean closeAll) {
		this.closeAll = closeAll;
	}

	public String getCloseMember() {
		return closeMember;
	}

	public void setCloseMember(String closeMember) {
		this.closeMember = closeMember;
	}

	public Date getCreateDate() {
		return createDate;
	}

	public void setCreateDate(Date createDate) {
		this.createDate = createDate;
	}

	public String getCreateBy() {
		return createBy;
	}

	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	
	
}
