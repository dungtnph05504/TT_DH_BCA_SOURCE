package com.nec.asia.nic.comp.trans.domain;

import java.io.Serializable;

public class TempPaymentDetail implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	private Long id;
	private String transactionId;
	private String typePayment;
	private String subtypePayment;
	private String statusFee;
	private Double paymentAmount;
	private String note;
	
	public TempPaymentDetail() {
		super();
	}

	public TempPaymentDetail(Long id, String transactionId, String typePayment,
			String subtypePayment, String statusFee, Double paymentAmount,
			String note) {
		super();
		this.id = id;
		this.transactionId = transactionId;
		this.typePayment = typePayment;
		this.subtypePayment = subtypePayment;
		this.statusFee = statusFee;
		this.paymentAmount = paymentAmount;
		this.note = note;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	
	
	public String getTransactionId() {
		return transactionId;
	}

	public void setTransactionId(String transactionId) {
		this.transactionId = transactionId;
	}

	public String getTypePayment() {
		return typePayment;
	}

	public void setTypePayment(String typePayment) {
		this.typePayment = typePayment;
	}

	public String getSubtypePayment() {
		return subtypePayment;
	}

	public void setSubtypePayment(String subtypePayment) {
		this.subtypePayment = subtypePayment;
	}

	public String getStatusFee() {
		return statusFee;
	}

	public void setStatusFee(String statusFee) {
		this.statusFee = statusFee;
	}

	public Double getPaymentAmount() {
		return paymentAmount;
	}

	public void setPaymentAmount(Double paymentAmount) {
		this.paymentAmount = paymentAmount;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}
	
	

}
