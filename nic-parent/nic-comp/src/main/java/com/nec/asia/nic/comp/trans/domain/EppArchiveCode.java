package com.nec.asia.nic.comp.trans.domain;

import java.io.Serializable;
import java.util.Date;

public class EppArchiveCode implements Serializable {

	private static final long serialVersionUID = 1L;
	private Long id;
	private String docType;
	private int nYear;
	private String officeCode;
	private int incNo;
	private int count;
	private String closed;
	
	public EppArchiveCode(){
		
	}
	
	public EppArchiveCode(Long id, String docType, int nYear,
			String officeCode, int incNo, int count, String closed) {
		super();
		this.id = id;
		this.docType = docType;
		this.nYear = nYear;
		this.officeCode = officeCode;
		this.incNo = incNo;
		this.count = count;
		this.closed = closed;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getDocType() {
		return docType;
	}

	public void setDocType(String docType) {
		this.docType = docType;
	}

	public int getnYear() {
		return nYear;
	}

	public void setnYear(int nYear) {
		this.nYear = nYear;
	}

	public String getOfficeCode() {
		return officeCode;
	}

	public void setOfficeCode(String officeCode) {
		this.officeCode = officeCode;
	}

	public int getIncNo() {
		return incNo;
	}

	public void setIncNo(int incNo) {
		this.incNo = incNo;
	}

	public int getCount() {
		return count;
	}

	public void setCount(int count) {
		this.count = count;
	}

	public String getClosed() {
		return closed;
	}

	public void setClosed(String closed) {
		this.closed = closed;
	}
	
	
}
