package com.nec.asia.nic.comp.trans.service.exception;

import com.nec.asia.nic.framework.exception.CoreException;

/**
 * @author chris
 *
 */
public class RejectionDataServiceException extends CoreException {
	
	/**
	 * Instantiates a new RejectionDataServiceException exception.
	 *
	 * @param message the message
	 */
	public RejectionDataServiceException(String message) {
		super(message);
		
	}

	/**
	 * Instantiates a new RejectionDataServiceException exception.
	 *
	 * @param cause the cause
	 */
	public RejectionDataServiceException(Throwable cause) {
		super(cause);
		
	}

	/**
	 * Instantiates a new RejectionDataServiceException exception.
	 *
	 * @param message the message
	 * @param cause the cause
	 */
	public RejectionDataServiceException(String message, Throwable cause) {
		super(message, cause);
		
	}
}
