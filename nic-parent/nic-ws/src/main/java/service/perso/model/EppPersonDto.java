package service.perso.model;

import java.sql.Date;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlRootElement(name="EppPersonDto")
@XmlType ( name = "EppPersonDto",  propOrder = { "id","name","searchName","gender","dateOfBirth","placeOfBirthId","nationalityId","type_", "picture" } )

public class EppPersonDto {
	private String id;
	private String name;
	private String searchName;
	private String gender;
	private String dateOfBirth;
	private String placeOfBirthId;
	private String nationalityId;
	private String type_;
	private byte[] picture;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSearchName() {
		return searchName;
	}
	public void setSearchName(String searchName) {
		this.searchName = searchName;
	}
	public String getGender() {
		return gender;
	}
	public void setGender(String gender) {
		this.gender = gender;
	}
	public String getDateOfBirth() {
		return dateOfBirth;
	}
	public void setDateOfBirth(String dateOfBirth) {
		this.dateOfBirth = dateOfBirth;
	}
	public String getPlaceOfBirthId() {
		return placeOfBirthId;
	}
	public void setPlaceOfBirthId(String placeOfBirthId) {
		this.placeOfBirthId = placeOfBirthId;
	}
	public String getNationalityId() {
		return nationalityId;
	}
	public void setNationalityId(String nationalityId) {
		this.nationalityId = nationalityId;
	}
	public String getType_() {
		return type_;
	}
	public void setType_(String type_) {
		this.type_ = type_;
	}
	public byte[] getPicture() {
		return picture;
	}
	public void setPicture(byte[] picture) {
		this.picture = picture;
	}
}
