package service.perso.model;

import java.sql.Date;

import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlRootElement(name="RetrieveDocumentDataRequest")
@XmlType ( name = "RetrieveDocumentDataRequest",  propOrder = { "siteCode", "passportNo", "issueDatePassport", "typePassport", "expireDatePassport", "handoverB" } )

public class RetrieveDocumentDataRequest {
	private String siteCode;
	private String passportNo;
	private String issueDatePassport;
	private String typePassport;
	private String expireDatePassport;
	private String handoverB;
	
	
	public String getSiteCode() {
		return siteCode;
	}
	public void setSiteCode(String siteCode) {
		this.siteCode = siteCode;
	}
	public String getPassportNo() {
		return passportNo;
	}
	public void setPassportNo(String passportNo) {
		this.passportNo = passportNo;
	}
	public String getIssueDatePassport() {
		return issueDatePassport;
	}
	public void setIssueDatePassport(String issueDatePassport) {
		this.issueDatePassport = issueDatePassport;
	}
	public String getTypePassport() {
		return typePassport;
	}
	public void setTypePassport(String typePassport) {
		this.typePassport = typePassport;
	}
	public String getExpireDatePassport() {
		return expireDatePassport;
	}
	public void setExpireDatePassport(String expireDatePassport) {
		this.expireDatePassport = expireDatePassport;
	}
	public String getHandoverB() {
		return handoverB;
	}
	public void setHandoverB(String handoverB) {
		this.handoverB = handoverB;
	}
	
}
