package service.transaction.model;

import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;




@XmlRootElement(name="EppBufPersonDoc")
@XmlType(name="EppBufPersonDoc", propOrder= {"transacionId", "docType", "serial", "docData"})
public class EppBufPersonDoc {
	
    private String transacionId;
    
    private String docType;
  
    private String serial;
   
    private byte[] docData;
   

	public String getTransacionId() {
		return transacionId;
	}

	public void setTransacionId(String transacionId) {
		this.transacionId = transacionId;
	}

	public String getDocType() {
		return docType;
	}

	public void setDocType(String docType) {
		this.docType = docType;
	}

	public String getSerial() {
		return serial;
	}

	public void setSerial(String serial) {
		this.serial = serial;
	}

	public byte[] getDocData() {
		return docData;
	}

	public void setDocData(byte[] docData) {
		this.docData = docData;
	}
    
}
