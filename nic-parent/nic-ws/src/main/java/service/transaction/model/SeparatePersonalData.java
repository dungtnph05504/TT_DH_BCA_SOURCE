package service.transaction.model;

import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlRootElement(name="SeparatePersonalData")
@XmlType(name="SeparatePersonalData", propOrder={"document", "orgDocument"})
public class SeparatePersonalData {

	private PersonOutput document;
	private OrgPersonOutput orgDocument;
	public PersonOutput getDocument() {
		return document;
	}
	public void setDocument(PersonOutput document) {
		this.document = document;
	}
	public OrgPersonOutput getOrgDocument() {
		return orgDocument;
	}
	public void setOrgDocument(OrgPersonOutput orgDocument) {
		this.orgDocument = orgDocument;
	}
	
}
