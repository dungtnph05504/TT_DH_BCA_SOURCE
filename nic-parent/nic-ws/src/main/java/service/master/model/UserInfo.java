package service.master.model;

import java.util.List;

import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;


@XmlRootElement(name="UserInfo")
@XmlType(name="UserInfo", propOrder={"userId","sysAdminFlag","siteCode","active","createDate","createBy","createWsktn","userName","position","department","password","email","roles"})
public class UserInfo {
	private String userId;
	private String sysAdminFlag;
	private String siteCode;
	private String active;
	private String createDate;
	private String createBy;
	private String createWsktn;
	private String userName;
	private String position;
	private String department;
	private String password;
	private String email;
	private List<String> roles;
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getSysAdminFlag() {
		return sysAdminFlag;
	}
	public void setSysAdminFlag(String sysAdminFlag) {
		this.sysAdminFlag = sysAdminFlag;
	}
	public String getSiteCode() {
		return siteCode;
	}
	public void setSiteCode(String siteCode) {
		this.siteCode = siteCode;
	}
	public String getActive() {
		return active;
	}
	public void setActive(String active) {
		this.active = active;
	}
	public String getCreateDate() {
		return createDate;
	}
	public void setCreateDate(String createDate) {
		this.createDate = createDate;
	}
	public String getCreateBy() {
		return createBy;
	}
	public void setCreateBy(String createBy) {
		this.createBy = createBy;
	}
	public String getCreateWsktn() {
		return createWsktn;
	}
	public void setCreateWsktn(String createWsktn) {
		this.createWsktn = createWsktn;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public List<String> getRoles() {
		return roles;
	}
	public void setRoles(List<String> roles) {
		this.roles = roles;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	
	
}
