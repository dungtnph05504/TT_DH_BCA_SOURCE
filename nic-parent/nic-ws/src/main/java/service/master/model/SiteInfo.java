package service.master.model;

import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;


@XmlRootElement(name="SiteInfo")
@XmlType(name="SiteInfo", propOrder={"siteId","siteFunction","country","siteName","address","active","groupId", "a08Id"})
public class SiteInfo {
	private String siteId;
	private String siteFunction;
	private String country;
	private String siteName;
	private String address;
	private String active;
	private String groupId;
	private Long a08Id;
	public String getSiteId() {
		return siteId;
	}
	public void setSiteId(String siteId) {
		this.siteId = siteId;
	}
	public String getSiteFunction() {
		return siteFunction;
	}
	public void setSiteFunction(String siteFunction) {
		this.siteFunction = siteFunction;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getSiteName() {
		return siteName;
	}
	public void setSiteName(String siteName) {
		this.siteName = siteName;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getActive() {
		return active;
	}
	public void setActive(String active) {
		this.active = active;
	}
	public String getGroupId() {
		return groupId;
	}
	public void setGroupId(String groupId) {
		this.groupId = groupId;
	}
	public Long getA08Id() {
		return a08Id;
	}
	public void setA08Id(Long a08Id) {
		this.a08Id = a08Id;
	}
	
	
	
}
