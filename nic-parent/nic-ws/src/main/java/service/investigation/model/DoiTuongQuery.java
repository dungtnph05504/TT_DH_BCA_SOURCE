package service.investigation.model;

import javax.xml.bind.annotation.XmlRootElement;
import javax.xml.bind.annotation.XmlType;

@XmlRootElement(name = "DoiTuongQuery")
@XmlType(name = "DoiTuongQuery", propOrder = { "hoTen", "ngaySinh",
		"kieuNgaySinh", "quocTich", "gioiTinh", "soHoChieu", "soCMND", "personCode"

})
public class DoiTuongQuery {
	private String hoTen;
	private String ngaySinh;
	private String kieuNgaySinh;
	private String quocTich;
	private String gioiTinh;
	private String soHoChieu;
	private String soCMND;
	private String personCode;

	public String getPersonCode() {
		return personCode;
	}

	public void setPersonCode(String personCode) {
		this.personCode = personCode;
	}

	public String getHoTen() {
		return hoTen;
	}

	public void setHoTen(String hoTen) {
		this.hoTen = hoTen;
	}

	public String getNgaySinh() {
		return ngaySinh;
	}

	public void setNgaySinh(String ngaySinh) {
		this.ngaySinh = ngaySinh;
	}

	public String getKieuNgaySinh() {
		return kieuNgaySinh;
	}

	public void setKieuNgaySinh(String kieuNgaySinh) {
		this.kieuNgaySinh = kieuNgaySinh;
	}

	public String getQuocTich() {
		return quocTich;
	}

	public void setQuocTich(String quocTich) {
		this.quocTich = quocTich;
	}

	public String getGioiTinh() {
		return gioiTinh;
	}

	public void setGioiTinh(String gioiTinh) {
		this.gioiTinh = gioiTinh;
	}

	public String getSoHoChieu() {
		return soHoChieu;
	}

	public void setSoHoChieu(String soHoChieu) {
		this.soHoChieu = soHoChieu;
	}

	public String getSoCMND() {
		return soCMND;
	}

	public void setSoCMND(String soCMND) {
		this.soCMND = soCMND;
	}

}
